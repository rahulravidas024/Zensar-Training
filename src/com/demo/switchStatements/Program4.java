package com.demo.switchStatements;

public class Program4 {

	public static void main(String[] args) {
		int k = 65;
		switch (k) {
		default:
			System.out.println("Website");
		case 65:
			System.out.println("Merit");
		case 'k':
			System.out.println("Campus");
		case 'j':
			System.out.println("Java");
			break;
		}

	}

}
